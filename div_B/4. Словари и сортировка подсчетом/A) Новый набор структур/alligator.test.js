const alligator = require('./alligator.js');

describe('Внутренние тесты контеста', () => {
	test('1', () => {
		expect(alligator('7\n1 5\n10 -5\n1 10\n4 -2\n4 3\n4 1\n4 0')).toBe('1 15\n4 2\n10 -5')
	});
	test('2', () => {
		expect(alligator('5\n5 -10000\n-5 100000000000\n10 2000000000000\n-5 -300000000000\n0 10000000000000')).toBe('-5 -200000000000\n0 10000000000000\n5 -10000\n10 2000000000000')
	});
});